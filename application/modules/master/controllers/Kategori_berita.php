    public function destroy()
    {
        if (check_permission(DELETE)) {
            $check_exist = $this->kategori_berita->get_single('fr_kategori_berita', array('id' => $this->input->post('id')));
            if ($check_exist) {
                $data = [
                    'is_deleted'    => 1,
                    'status'        => 0,
                    'deleted_at'    => date('Y-m-d H:i:s'),
                    'deleted_by'    => logged_in_user_id(),
                ];
                if ($check_exist->is_deleted) {
                    $this->kategori_berita->delete('fr_kategori_berita', array('id' => $this->input->post('id')));
                } else {
                    $this->kategori_berita->update('fr_kategori_berita', $data, array('id' => $this->input->post('id')));
                }
                $response = [
                    'status'    => 200,
                    'modular'   => 'master',
                    'module'    => 'kategori_berita',
                    'socket'    => 'master_kategori_berita',
                    'action'    => 'delete',
                    'user'      => $this->session->userdata('fullname'),
                    'message'   => 'Your data has been deleted',
                ];
            } else {
                $response = [
                    'status'    => 403,
                    'modular'   => 'master',
                    'module'    => 'kategori_berita',
                    'socket'    => 'master_kategori_berita',
                    'action'    => 'delete',
                    'user'      => $this->session->userdata('fullname'),
                    'message'   => 'Delete data FAILED, Please try again',
                ];
            }
            echo json_encode($response);
        }
    }


